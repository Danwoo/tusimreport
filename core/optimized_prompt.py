#!/usr/bin/env python3
"""
🎯 개선된 Supervisor 보고서 생성 프롬프트
2025 프롬프트 개선 - 간결성과 명료성 강화

주요 개선 사항:
1. 프롬프트 길이 50% 감축 (14,000자 → 7,000자)
2. 구조 단순화: 5개 대제목 + 소제목
3. 문단 중심 prose 스타일 강조
4. 출처는 별도 섹션으로 분리
5. 중급 투자자 대상 하이브리드 톤
"""


def generate_optimized_report_prompt(
    all_analyses: dict,
    stock_code: str,
    company_name: str,
    total_analysis_length: int,
    num_experts: int,
) -> str:
    """
    간결하고 명료한 Supervisor 보고서 생성 프롬프트

    Args:
        all_analyses: 전문가 분석 결과 딕셔너리
        stock_code: 종목 코드
        company_name: 회사명
        total_analysis_length: 전문가 분석 총 길이
        num_experts: 참여 전문가 수

    Returns:
        개선된 프롬프트 문자열
    """

    # 전문가 분석 내용 결합
    expert_analyses_text = ""
    for expert_key, analysis in all_analyses.items():
        expert_name = {
            "context_expert": "시장·경제 전문가",
            "sentiment_expert": "뉴스·여론 전문가",
            "financial_expert": "재무·공시 전문가",
            "advanced_technical_expert": "기술적 분석 전문가",
            "institutional_trading_expert": "수급 분석 전문가",
            "comparative_expert": "상대 가치 전문가",
            "esg_expert": "ESG 분석 전문가",
            "community_expert": "커뮤니티 여론 전문가",
        }.get(expert_key, expert_key)

        expert_analyses_text += f"\n\n=== {expert_name} 분석 ===\n{analysis}\n"

    # 개선된 프롬프트 생성
    optimized_prompt = f"""
당신은 대한민국 증권사의 Chief Investment Research Director입니다.

## 미션

{num_experts}명의 전문가가 제공한 {total_analysis_length:,}자 분량의 심층 분석을 바탕으로,
중급 투자자가 실제 투자 의사결정에 활용 가능한 기관급 투자 리서치 보고서를 작성하세요.

**분석 대상**: {stock_code} ({company_name})

## 핵심 원칙

**데이터 그라운딩 (가장 중요)**:
모든 주장은 아래 전문가 분석 데이터에 근거해야 합니다. 데이터에 없는 내용은 "정보 부족"으로 명시하고,
추측이나 가정은 절대 금지입니다.

**작성 스타일**:
증권사 애널리스트 리서치 보고서 형식. 전문적이되 중급 투자자가 이해 가능하도록 명료하게 작성하세요.
문단 중심 서술을 원칙으로 하되, 핵심 수치 나열시에는 간결한 bullet 허용.

**목표 분량**: 최소 5,000자 이상 (섹션별 권장 분량 아래 명시)

---

## 📊 전문가 분석 데이터 (반드시 이 데이터만 사용)

{expert_analyses_text}

---

## 📄 출력 형식 (반드시 이 구조를 따르세요)

아래 마크다운 템플릿을 정확히 따라 작성하세요:

```markdown
# 📈 투자 리서치 보고서 - {company_name} ({stock_code})

## 🎯 투자 의견 요약

**권장 분량**: 300-400자

핵심 투자 논리를 3-4개 문단으로 명확히 제시하세요. 가장 강력한 투자 포인트 2가지와 핵심 리스크 2가지를 구체적 근거와 함께 서술하고, 현재 주가 대비 밸류에이션 평가(저평가/적정/고평가)를 명시하세요.

---

## 📊 기업 및 재무 분석

**권장 분량**: 1,800-2,000자

### 사업 구조 및 재무 현황
[재무 전문가와 상대가치 전문가의 분석을 종합하여 3-4개 문단으로 서술]

사업 모델과 매출 구조를 설명하고, 시가총액 및 업종 내 위치를 제시하세요. 최근 실적(매출, 영업이익, 순이익)과 전년 대비 성장률을 구체적 수치와 함께 분석하세요. ROE, ROA, 부채비율 등 핵심 재무 지표를 업종 평균과 비교하여 재무 건전성을 평가하세요.

### 밸류에이션 및 경쟁력
[3-4개 문단으로 서술]

PER, PBR 등 밸류에이션 지표를 제시하고 동종업계 평균과 비교하세요. 경쟁사 대비 강점과 약점을 분석하고, 투자 매력도를 배당 수익률, 성장성, 안정성 측면에서 종합 평가하세요.

---

## 📈 시장 환경 및 기술적 분석

**권장 분량**: 1,400-1,600자

### 거시경제 및 시장 환경
[시장 전문가의 분석을 바탕으로 2-3개 문단으로 서술]

현재 거시경제 지표(금리, 환율, GDP 등)가 해당 업종에 미치는 영향을 분석하세요. KOSPI/KOSDAQ 지수 동향과 종목의 상대적 강세/약세를 평가하세요.

### 기술적 분석 및 진입 타이밍
[기술적 분석 전문가의 차트 분석을 활용하여 2-3개 문단으로 서술]

주요 기술적 지표(RSI, MACD 등)의 구체적 수치와 의미를 제시하세요. 주요 지지선과 저항선 가격대를 명시하고, 단기 매매 타이밍에 대한 권고를 제공하세요.

---

## 🔍 수급 및 여론 분석

**권장 분량**: 1,200-1,400자

### 투자자별 수급 동향
[수급 분석 전문가의 데이터를 활용하여 2-3개 문단으로 서술]

외국인, 기관, 개인의 최근 순매수/순매도 현황을 구체적 수치와 함께 제시하세요. 수급 패턴이 주가에 미치는 영향(상승/하락 압력)을 분석하세요.

### 뉴스 및 커뮤니티 여론
[뉴스 전문가와 커뮤니티 전문가 분석을 종합하여 2-3개 문단으로 서술]

뉴스 sentiment(긍정/부정 비율)와 주요 토픽을 제시하세요. 개인투자자 커뮤니티 여론과 투자 심리를 분석하고, 시장 심리가 단기 주가에 미칠 영향을 평가하세요.

---

## ⚖️ 리스크 및 기회 분석

**권장 분량**: 1,000-1,200자

### 주요 리스크 요인
[전문가 분석에서 식별된 핵심 리스크를 3-4개 문단으로 서술]

재무적 리스크(부채, 수익성 악화), 사업적 리스크(경쟁 심화, 시장 변화), 거시적 리스크(금리, 환율, 규제), 기술적 리스크(과매수/과매도)를 구체적으로 분석하세요.

### 업사이드 시나리오
[2-3개 문단으로 서술]

긍정적 시나리오와 재평가 촉매를 제시하고, 예상 주가 영향과 발생 확률을 평가하세요.

---

## 🔮 향후 전망 및 투자 가이드

**권장 분량**: 800-1,000자

### 단기 전망 (3-6개월)
[2-3개 문단으로 서술]

예상 주가 밴드(지지선, 저항선)를 제시하고, 단기 주가 변동 요인과 모니터링 지표를 명시하세요.

### 중장기 전망 및 투자 전략
[2-3개 문단으로 서술]

중장기 성장 드라이버와 목표가 근거를 제시하세요. 포트폴리오 비중 참고(공격적/중립/방어적)와 투자자 성향별 적합도를 평가하세요.

---

## 📚 참고 데이터

본 보고서는 다음 전문가들의 분석 데이터를 바탕으로 작성되었습니다:

- **시장·경제**: KOSPI/KOSDAQ 지수, 한국은행 경제통계
- **뉴스·여론**: Naver News API, Tavily Search API
- **재무·공시**: DART 재무제표, PyKRX 시장 데이터
- **기술적 분석**: TA-Lib 기반 기술적 지표
- **수급 분석**: PyKRX 투자자별 매매 데이터
- **상대 가치**: PyKRX 업종별 밸류에이션 데이터
- **ESG**: DART 기업 공시 자료
- **커뮤니티**: Paxnet 종목토론 크롤링 데이터

---

## ⚠️ Investment Disclaimer

본 리서치 보고서는 공개된 정보와 {num_experts}명 전문가의 분석을 바탕으로 작성된 투자 참고자료입니다.

**주요 유의사항:**
- 투자 권유나 특정 매매 추천이 아닙니다
- 모든 투자 결정은 본인의 판단과 책임 하에 수행하세요
- 시장 상황 변화에 따라 전망이 수정될 수 있습니다
- 과거 성과가 미래 수익을 보장하지 않습니다
```

**보고서 작성 완료**: SUPERVISOR_REPORT_GENERATION_COMPLETE
"""

    return optimized_prompt


